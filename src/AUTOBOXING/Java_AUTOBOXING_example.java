package AUTOBOXING;

public class Java_AUTOBOXING_example {

    public static void main(String[] args) {
        Integer i = 100; //autoboxing
        int i_2 = i; // auto-unbox

        System.out.println("Autoboxing Integer i: " +  i);
        System.out.println("Auto-unbox int i_1: " +  i_2);
    }
}
